@Injectable({ providedIn: 'root' })
export class I18nService {
  readonly lang = signal<AppLang>('en');

  readonly isRtl = computed(() => false);

  t(key: string): string {
    const L = DICT[this.lang()] ?? DICT.en;
    return L[key] ?? DICT.en[key] ?? key;
  }

  setLang(code: AppLang): void {
    this.lang.set(code);
    document.documentElement.lang = code;
  }
}
